export const getRandomAZHousePolitician = (usedAnswers) => {
    if(usedAnswers.length === housePoliticiansInDistrictOrder.length) return;
    let tempAnswer = housePoliticiansInDistrictOrder[Math.floor(Math.random()*housePoliticiansInDistrictOrder.length)];
    while(usedAnswers.find(answer => answer.name === tempAnswer.name)){
        tempAnswer = housePoliticiansInDistrictOrder[Math.floor(Math.random()*housePoliticiansInDistrictOrder.length)];
    }
    return tempAnswer;
}